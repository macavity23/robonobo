package com.robonobo.wang;

public class WangServerException extends WangException {

	public WangServerException() {
		super();
	}

	public WangServerException(String message, Throwable cause) {
		super(message, cause);
	}

	public WangServerException(String message) {
		super(message);
	}

	public WangServerException(Throwable cause) {
		super(cause);
	}

}
