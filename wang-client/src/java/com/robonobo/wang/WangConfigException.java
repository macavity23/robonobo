package com.robonobo.wang;

public class WangConfigException extends WangException {

	public WangConfigException() {
	}

	public WangConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public WangConfigException(String message) {
		super(message);
	}

	public WangConfigException(Throwable cause) {
		super(cause);
	}

}
