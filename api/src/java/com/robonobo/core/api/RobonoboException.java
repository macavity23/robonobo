package com.robonobo.core.api;

public class RobonoboException extends Exception {
	private static final long serialVersionUID = 4418833337476146960L;

	public RobonoboException() {
		super();
	}

	public RobonoboException(String arg0) {
		super(arg0);
	}

	public RobonoboException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RobonoboException(Throwable arg0) {
		super(arg0);
	}

}
